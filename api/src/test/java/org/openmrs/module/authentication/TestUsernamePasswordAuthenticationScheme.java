package org.openmrs.module.authentication;

import org.openmrs.User;
import org.openmrs.api.context.Authenticated;
import org.openmrs.api.context.BasicAuthenticated;
import org.openmrs.api.context.ContextAuthenticationException;
import org.openmrs.api.context.Credentials;
import org.openmrs.api.context.UsernamePasswordAuthenticationScheme;

/**
 * Represents a particular method of authentication.
 */
public class TestUsernamePasswordAuthenticationScheme extends UsernamePasswordAuthenticationScheme {

    @Override
    public Authenticated authenticate(Credentials credentials) throws ContextAuthenticationException {
        User u = new User();
        u.setUsername(credentials.getClientName());
        return new BasicAuthenticated(u, credentials.getAuthenticationScheme());
    }
}
